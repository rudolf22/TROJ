% --- Executes on button press in pushbutton3.
function [z,T,V] = pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
pressure = get(handles.edit4,'String');
temperature = get(handles.edit5,'String');
volume = get(handles.edit7,'String');
volume = str2num(volume);
pressure = str2double(pressure);
temperature = str2double(temperature);
listBox2Data_s = get(handles.listbox2,'String');
listBox3Data_s = get(handles.listbox3,'String');
len = size(listBox2Data_s);
for k = 1:len(1)
    listBox2Data = strtrim(listBox2Data_s(k,:));
    listBox3Data = strtrim(listBox3Data_s(k,:));
    id(k,:) = get_id(listBox2Data);
    Ff(k,:) = str2num(listBox3Data);
end
[z,T,V] = reaktor_launch(temperature,pressure,id,Ff,volume);
assignin('base', 'z', z)
assignin('base', 'T', T)
assignin('base', 'V', V)
for k = 1:length(z)
    plot(z(1:k),T(1:k))
    set(handles.edit6,'String',T(k))
    pause(0.1)
end


